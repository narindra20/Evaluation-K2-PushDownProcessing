public class PieceVendusModele {

    private int getz;
    private int pride;
    private int lacetti;

    public PieceVendusModele(int getz, int pride, int lacetti) {
        this.getz = getz;
        this.pride = pride;
        this.lacetti = lacetti;
    }

    public int getGetz() {
        return getz;
    }

    public int getPride() {
        return pride;
    }

    public int getLacetti() {
        return lacetti;
    }
}